'''
Escreva um algoritmo que recebe um número x e y e imprime x elevado y. Lembre-se que ** representa
a potência entre dois números em Python.

'''

numeroX = float(input("Informe um numero: "))
numeroY = float(input("Informe outro numero para ser elevado: "))

potencia = numeroX ** numeroY

print("Potencia: ", potencia) 